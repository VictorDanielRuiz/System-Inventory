//Switch-Case

let q1 = 10;

switch(q1){
    case 10:
        console.log("a");
        break;
    case 9:
        console.log("b");
        break;
    case 8:
        console.log("c");
        break;
    case 7:
        console.log("d");
        break;
    case 6:
        console.log("e");
        break;
    case 5:
        console.log("f");
        break;
    case 4:
        console.log("g");
        break;
    default:
        console.log("error!")
        break;
}
